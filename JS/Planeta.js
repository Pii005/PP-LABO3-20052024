class planeta
{
    constructor(id, nombre, tamaño, masa, tipo, distancia, presentaVida, anillo, composicion) {
        this.id = id;
        this.nombre = nombre;
        this.tamaño = tamaño;
        this.masa = masa;
        this.tipo = tipo;
        this.distancia = distancia;
        this.presentaVida = presentaVida;
        this.anillo = anillo;
        this.composicion = composicion;
    }
}




export { planeta };